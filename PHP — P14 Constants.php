var_dump(CAR);//Обычный вызов в браузер массива с полным его описанием и значениями, обязательно в круглых скобках, при обращении по названию массива, НЕ ИСПОЛЬЗОВАТЬ КАВЫЧКИ. 

//Вам не нужно использовать функцию определения при создании констант. Вы можете поставить перед именем константы ключевое слово const.

